<!-- 1. Создайте HTML-таблицу, в которой каждая строка будет содержать кнопку удаления. Например: -->

<table>
  <tr>
    <th>Имя</th>
    <th>Email</th>
    <th>Действие</th>
  </tr>
  <tr>
    <td>John</td>
    <td>john@example.com</td>
    <td><button onclick="deleteRow(1)">Удалить</button></td>
  </tr>
  <tr>
    <td>Jane</td>
    <td>jane@example.com</td>
    <td><button onclick="deleteRow(2)">Удалить</button></td>
  </tr>
  <!-- Добавьте другие строки -->
</table>


<!-- 2. Добавьте JavaScript-функцию deleteRow() для обработки нажатия кнопки удаления и передачи идентификатора строки в PHP-скрипт. Например: -->

<script>
  function deleteRow(rowId) {
    if (confirm('Вы уверены, что хотите удалить эту строку?')) {
      // Передача идентификатора строки в PHP-скрипт
      window.location.href = 'delete_row.php?id=' + rowId;
    }
  }
</script>


<!-- 3. Создайте PHP-скрипт delete_row.php, который будет выполнять удаление строки из базы данных на основе переданного идентификатора. Например: -->

<?php
// Подключение к базе данных
$db = new PDO('mysql:host=localhost;dbname=your_database', 'username', 'password');

// Получение переданного идентификатора строки
$id = $_GET['id'];

// Удаление строки из таблицы
$query = "DELETE FROM your_table WHERE id = :id";
$statement = $db->prepare($query);
$statement->execute(['id' => $id]);

// Перенаправление обратно на страницу с таблицей
header('Location: table.php');
?>


<!-- Обратите внимание, что вам нужно будет заменить your_database, username, password, your_table и table.php на соответствующие значения для вашей базы данных и таблицы. -->

<!-- Теперь, при нажатии кнопки "Удалить" в строке таблицы, скрипт будет отправлять запрос на удаление строки из базы данных и перенаправлять пользователя обратно на страницу с таблицей. -->