<?
// В PHP можно использовать функцию echo для вывода данных в консоль. Вот несколько способов вывода в консоль в PHP:

// 1. Использование функции echo:
echo "Текст для вывода в консоль";


// 2. Использование функции print:
print "Текст для вывода в консоль";


// 3. Использование оператора print_r для вывода массивов и объектов:
$array = [1, 2, 3, 4, 5];
print_r($array);


// 4. Использование функции var_dump для вывода подробной информации о переменных:
$number = 10;
var_dump($number);


// 5. Использование функции printf для форматированного вывода:
$name = "John";
$age = 25;
printf("Меня зовут %s и мне %d лет", $name, $age);


// 6. Использование функции fwrite для вывода в стандартный поток вывода:
$fp = fopen('php://stdout', 'w');
fwrite($fp, "Текст для вывода в консоль");
fclose($fp);


// Обратите внимание, что вывод в консоль будет виден только при запуске PHP сценария из командной строки или терминала. Если вы запустите PHP сценарий в веб-сервере, вывод будет отправлен в логи сервера или не будет виден вовсе.
?>