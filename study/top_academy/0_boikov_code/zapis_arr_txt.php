<?
// Чтобы записать массив в файл txt в PHP, вы можете использовать функцию file_put_contents. Вот пример:

$data = array('apple', 'banana', 'orange');

// Преобразование массива в строку
$content = implode("\n", $data);

// Запись строки в файл
file_put_contents('file.txt', $content);


// В этом примере массив $data содержит элементы 'apple', 'banana' и 'orange'. Функция implode используется для преобразования массива в строку, где каждый элемент разделен символом новой строки "\n". Затем функция file_put_contents записывает эту строку в файл file.txt.

// Если файл file.txt уже существует, он будет перезаписан. Если вы хотите добавить данные в конец файла, а не перезаписывать его, вы можете использовать функцию file_put_contents вместе с флагом FILE_APPEND:

file_put_contents('file.txt', $content, FILE_APPEND);


// В этом случае данные будут добавлены в конец файла file.txt, а не перезаписаны.